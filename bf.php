<strong><p>History</p><br/>
Uttara University, situated in the outskirts of Dhaka City, is a center of excellence for tertiary education in Bangladesh. It started in 2003 with a few 
  students and departments. By now, Uttara University has grown as a full-fledged university offering under-graduate and graduate programs and research 
  opportunities. <br/><br/>It is nationally and internationally recognized for its motto of quality education at affordable tuition. The graduates of Uttara University 
  are well placed in reputed corporate sector of Bangladesh. In this cosmopolitan campus, students from all walks of life can have access to the tertiary 
  education and avail the opportunity to face the global challenges. To upgrade the students academically (using international collaboration), with etiquette 
  and grooming the prime objective is to help grow students professionally.<br/> <br/>The university helps them prepare for demanding corporate fields along with the 
  development of their professional and rational skills. Uttara University was founded by a Trustee Board. The members of the TRUST are educationists, 
  economists and businessmen of eminence and recognition. Prof. Dr. M. Azizur Rahman is the Vice-Chancellor and founder Chairman of the Trustee Board and
  Prof. Dr. Eaysmin Ara Lekha is the founder Vice-Chairman of the Board and Pro-Vice-Chancellor of the University.<br/>

Prof. Dr. M. Azizur Rahman, founder of Uttara University, is a former economic adviser of the US Embassy (Dhaka), a renowned academician of the country and
 an eminent economist of international repute, and an alumnus of Vanderbilt University, a Nobel Laureate school in USA. He possesses ingenuity in national 
 education that benefits the students who would like to build up their sustainable career in modem global market. In this spirit, Dr. Rahman established a good number of educational institutions including Uttara University where meritorious and enthusiastic students are benefited by using their merit with qualitative ingredients.

Prof. Dr. Eaysmin Ara Lekha is the first Pro-Vice-Chancellor of Uttara University. <br/><br/>In recognition of her meritorious service and experience in the field of
 education, she has been appointed as Pro-Vice-Chancellor by the honorable Chancellor and the President of the Peoples republic of Bangladesh. Before joining
 as Pro-Vice-Chancellor, she was the Dean of “School of Education and Physical Education”. She is the author of a number of books. As a patron of learning
 she has established a couple of educational institutions including Sabira Rouf College, Gopalgonj.<br/><br/></strong>
